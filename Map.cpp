Map::Map ()
{
    ground = TextureManager::LoadTexture("Image/ground.png");
    grass = TextureManager::LoadTexture("Image/grass.png");
    pathwgrass = TextureManager::LoadTexture("Image/pathwgrass.png");

    LoadMap(lvl1);

    src.x = src.y = 0;
    src.w = src.h = 64;

    dest.x = 1;
    dest.y = 1;
    dest.h = 64;
    dest.w = 64;
}
void Map::LoadMap(int arr[20][25])
{
    for (int row = 0; row < 20; row ++)
    {
        for (int col = 0; col < 25; col++)
        {
          map_[row][col] = arr[row][col];
        }
    }
}
void Map::DrawMap()
{
    int type = 0;
     for (int row = 0; row < 20; row ++)
    {
        //dest.x = col*64;
        for (int col = 0; col < 25; col++)
        {
          type = map_[row][col];
          dest.x = col*64;
          dest.y = row*64;
          switch (type)
          {
          case 0:
            TextureManager::Draw(grass, src, dest);
            break;
          case 1:
            TextureManager::Draw(ground, src, dest);
            break;
          case 2:
            TextureManager::Draw(pathwgrass, src, dest);
            break;
          default:
            break;
          }
        }
    }
}
